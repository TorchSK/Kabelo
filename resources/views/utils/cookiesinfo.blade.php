<h3>Čo sú súbory cookie?</h3>
<div>Súbor cookie je malý textový súbor, ktorý webová lokalita ukladá vo vašom počítači alebo mobilnom zariadení pri jej prehliadaní. Vďaka tomuto súboru si webová lokalita na určitý čas uchováva informácie o vašich krokoch a preferenciách (ako sú prihlasovacie meno, jazyk, veľkosť písma a iné nastavenia zobrazovania), takže ich pri ďalšej návšteve lokality alebo prehliadaní jej jednotlivých stránok nemusíte opätovne uvádzať.</div>

<h3>Ako používame súbory cookie?</h3>
<div>Tieto webstránky používajú súbory cookies na zapamätanie si použivateľských nastavení, pre lepšie prispôsobenie reklám záujmom návštevníkov a pre nevyhnutnú funkcionalitu webstránok.</div>

<h3>Ako kontrolovať súbory cookie</h3>
<div>Súbory cookie môžete kontrolovať alebo zmazať podľa uváženia – podrobnosti si pozrite na stránke aboutcookies.org.  Môžete vymazať všetky súbory cookie uložené vo svojom počítači a väčšinu prehliadačov môžete nastaviť tak, aby ste im znemožnili ich ukladanie. V takomto prípade však pravdepodobne budete musieť pri každej návšteve webovej lokality manuálne upravovať niektoré nastavenia a niektoré služby a funkcie nebudú fungovať.</div>

